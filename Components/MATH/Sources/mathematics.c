float Math_Sinf(uint16_t au16_angle)
{
  uint16_t index;
  index = (uint16_t)(au16_angle / MATH_SINTABLE_INDEXER);
  return f_SinusTable256[index];
}

float Math_Cosf(uint16_t au16_angle)
{
  uint16_t index;
  uint16_t cosangle = (uint16_t)(au16_angle + MATH_PIOVER2_DIG);
  index = (uint16_t)(cosangle / MATH_SINTABLE_INDEXER);
  return f_SinusTable256[index];
}
